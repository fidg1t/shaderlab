#include <iostream>
#include "window.hpp"
#include "renderer.hpp"

//-----------------------------------------------------------------------------
// Defines
//-----------------------------------------------------------------------------

//-----------------------------------------------------------------------------
// Functions
//-----------------------------------------------------------------------------

int main(void)
{
  Renderer renderer;

  renderer.Init();

  double last = 0;

  while (!renderer.ShouldExit())
  {
    double now = glfwGetTime();
    double dt = now - last;
    last = now;

    renderer.Update(dt);
    renderer.Render();
  }

  renderer.Exit();

  return 0;
}